//! ==
//? only checks for value, does not check the data type
console.log(10 == 10);
console.log(10 == "10")

//! ===
//? check the value, along with that check the data type also
console.log(10 === 10)
console.log(10 === "10")

//! !=
//? check the value only, not the data type
console.log(10 != 10)
console.log(10 != "10")

//! !==
//? check the data type along with the value
console.log(10 !== 10)
console.log(10 !== "10")
